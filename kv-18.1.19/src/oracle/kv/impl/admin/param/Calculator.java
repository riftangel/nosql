import java.lang.annotation.Target;

/**
 * Marks the method that initializes calculated fields in an object.  This is
 * used in the KVAdmin client, when setting fields of a parameters object.
 */
@Retention(RetentionPolicy.RUNTIME)
@Target(ElementType.METHOD)
public @interface Calculator { }
